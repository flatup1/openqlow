/**
 * 全角スラッシュ「／」も半角「/」と同等に扱う。
 */
function normalise(text: string): string {
  return text.trim().replace(/^[／\/\\]/, "/");
}

/**
 * 「今何してる」「今何」「妻向け」など、オーナー情報コマンドを検出する。
 */
export function isOwnerInfoCommand(text: string): boolean {
  const t = normalise(text);
  return /^\/?(?:今何|今何してる|今なに|今なにしてる|何してる|何やってる|妻向け|妻に|家族向け|家族に|説明|何作ってる|何作ってるの)\??$/.test(t);
}

export function getOwnerInfoReply(): string {
  return FAMILY_EXPLANATION;
}
